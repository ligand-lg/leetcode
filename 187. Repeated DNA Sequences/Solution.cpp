class Solution {
public:
  int encode(char c) {
    if (c == 'A') {
      return 0;
    }
    if (c == 'C') {
      return 1;
    }
    if (c == 'G') {
      return 2;
    }
    if (c == 'T') {
      return 3;
    }
    return -1;
  }
  string decode(int code) {
    string s;
    char c = '?';

    while (s.length() != 10) {
      int mul = code / 4;
      int remainder = code - (mul * 4);
      if (remainder == 0) {
        c = 'A';
      } else if (remainder == 1) {
        c = 'C';
      } else if (remainder == 2) {
        c = 'G';
      } else if (remainder == 3) {
        c = 'T';
      }
      s = c + s;
      code = mul;
    }
    return s;
  }
  vector<string> findRepeatedDnaSequences(string s) {
    const int INTERVEL = 10;
    vector<string> res;

    if (s.length() < INTERVEL + 1) {
      return res;
    }

    vector<int> hashCodes(s.length(), -1);
    // init
    int code = 0;
    size_t end = 0;
    for (; end < 10; ++end) {
      code = code * 4 + encode(s[end]);
    }
    hashCodes[end - 1] = code;
    for (; end < s.length(); ++end) {
      code -= encode(s[end - 10]) * 262144; // 4^9
      code = code * 4 + encode(s[end]);
      hashCodes[end] = code;
    }

    set<int> repeated;
    for (size_t i = 9; i < hashCodes.size(); ++i) {
      if (repeated.find(hashCodes[i]) == repeated.end()) {
        for (size_t j = hashCodes.size() - 1; j > i; --j) {
          if (hashCodes[j] == hashCodes[i]) {
            repeated.insert(hashCodes[i]);
            res.push_back(decode(hashCodes[i]));
            break;
          }
        }
      }
    }
    return res;
  }
};

